package org.apache.nsjk2;

/**
 * <P>NsLog provides a java wrapper around the aolserver Ns_ModLog command. By
 * default all output shows up in the log file as 'nsjava' entries.
 *
 * @author Daniel Wickstrom
 * @author Alexander Leykekh
 * @see NsTcl
 */
public class NsLog {

  public static final byte NOTICE=0;
  public static final byte WARNING=1;
  public static final byte ERROR=2;
  public static final byte FATAL=3;
  public static final byte BUG=4;
  public static final byte DEBUG=5;


  /**
   * Write a message to the aolserver log file with an associated severity 
   * level.
   *
   * @param severity can be one of Notice, Warning, Error, Fatal, Bug, 
   * or Debug.
   * @param message message to output to the log file.
   */
  public native void write(byte severity, String message);

  static 
  {
      try 
      {
	System.loadLibrary("nsjk2");
      } 
      catch (Throwable e) 
	{
	System.err.println("Cannot load library nsjk2. Exception: "+e.getClass().getName()+" Message: "+e.getMessage());
	}
  }  
}
